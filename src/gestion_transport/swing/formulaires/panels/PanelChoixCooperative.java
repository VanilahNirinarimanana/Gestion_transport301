    public void loadData(){
        if(parent.temp.dateDepart == null){
            return;
        }
        else{
            remplirCooperatives();
            ImageIcon icon = new ImageIcon(getClass().getResource("/images/ChoixCoop.png"));
            Image img = icon.getImage().getScaledInstance(500, 250, Image.SCALE_SMOOTH);
            lblChoixCooperative.setIcon(new ImageIcon(img));
        }

    }
    
    private void remplirCooperatives() {
        //cbCooperative.removeAllItems();
        try{
            cbChoixCooperative.removeAllItems();
            System.out.println(parent.temp.villeArrivee);
            System.out.println(parent.temp.dateDepart);
            GestionCooperative gc = new GestionCooperative();
            ResultSet rs = gc.getCooperative(parent.temp.villeArrivee, Date.valueOf(parent.temp.dateDepart));
            while (rs.next()) {
                int id_cooper = rs.getInt("c.id_cooper");
                String nom = rs.getString("c.nom_coo");
                cbChoixCooperative.addItem(id_cooper + " - " + nom);
            }
        }catch(Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erreur de chargement des coopératives");
        }
    }

