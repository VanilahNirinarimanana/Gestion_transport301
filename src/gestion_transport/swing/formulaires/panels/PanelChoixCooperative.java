    private void btnSuivantCoopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuivantCoopActionPerformed
        // TODO add your handling code here:
        if (cbChoixCooperative.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Veuillez sélectionner une coopérative.");
            return;
        }

        String selected = cbChoixCooperative.getSelectedItem().toString();
        int idCoop = Integer.parseInt(selected.split(" - ")[0]);
        String nom = selected.split(" - ")[1].trim();
        parent.temp.cooperative = nom;
        parent.temp.idCoop = idCoop;

        parent.goTo("choix_siege"); // Passage au panel suivant
    }//GEN-LAST:event_btnSuivantCoopActionPerformed

