    private void btnSuivantSiegeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuivantSiegeActionPerformed
        // TODO add your handling code here:
        List<Integer> siegesChoisis = new ArrayList<>();

        for (JCheckBox cb : checkBoxes) {
            if (cb.isSelected()) {
                siegesChoisis.add((Integer) cb.getClientProperty("id_siege"));
            }
        }

        if (siegesChoisis.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Veuillez sélectionner au moins un siège.");
            return;
        }

        if (siegesChoisis.size() > 5) {
            JOptionPane.showMessageDialog(this, "Vous ne pouvez choisir que 5 sièges maximum.");
            return;
        }

        parent.temp.placesChoisies = siegesChoisis;
        parent.temp.nb_billet = siegesChoisis.size();
        //System.out.println(parent.temp.nb_billet);

        parent.goTo("info_passager");

    }//GEN-LAST:event_btnSuivantSiegeActionPerformed

