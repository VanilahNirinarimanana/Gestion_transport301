    public void loadDataSiege(){
        if(parent.temp.dateDepart == null){
            return;
        }
        else{
            panelSiege.setMaximumSize(new Dimension(400, 200));
            panelSiege.setLayout(new GridLayout(0, 5, 10, 10));
            chargerSiegesDisponibles();
            ImageIcon icon = new ImageIcon(getClass().getResource("/images/ChoixSiege.png"));
            Image img = icon.getImage().getScaledInstance(500, 300, Image.SCALE_SMOOTH);
            lblChoxSiege.setIcon(new ImageIcon(img));
        }

    }
    
    private void chargerSiegesDisponibles() {
        panelSiege.removeAll();
        checkBoxes.clear();

        try{
            GestionSiege gs = new GestionSiege();
            ResultSet rs = gs.selectSiege(parent.temp.villeArrivee, java.sql.Date.valueOf(parent.temp.dateDepart), parent.temp.idCoop);
            if(!rs.next()){
                JOptionPane.showMessageDialog(this, "Aucun siège disponible pour cette coopérative");
                return;
            }
            do{
                int idSiege = rs.getInt("s.id_siege");
                String numero = rs.getString("s.num_siege");

                JCheckBox cb = new JCheckBox(numero);
                cb.putClientProperty("id_siege", idSiege);
                checkBoxes.add(cb);
                panelSiege.add(cb);
            }while (rs.next());
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erreur lors du chargement des sièges");
        }

        panelSiege.revalidate();
        panelSiege.repaint();
    }



