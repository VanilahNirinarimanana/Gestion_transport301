    public boolean verifierAdmin(){
        if(txtNomAdmin.getText().trim().isEmpty() || txtConAdmin.getText().trim().isEmpty() || txtMdpAdmin.getPassword().equals("")){
            JOptionPane.showMessageDialog(this, "Veuiller remplir tous les champs obligatoires", "Champs manquants", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        if(!txtConAdmin.getText().matches("^(034|032|033|038|037)\\d{7}$")){
            JOptionPane.showMessageDialog(this, "Le contact doit comporter 10 chiffres et commencer par 034, 032, 033, 038 ou 037", "Numéro Invalide", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        if(txtMdpAdmin.getPassword().length < 8){
            JOptionPane.showMessageDialog(this, "Le mot de passe doit comporter 8 caractères", "Mot de passe Invalide", JOptionPane.WARNING_MESSAGE);
        }
        return true;
    }

