    private void txtNomAdminKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNomAdminKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if(!Character.isLetterOrDigit(c) && c != ' '){
            evt.consume();
        }
    }//GEN-LAST:event_txtNomAdminKeyTyped

    private void txtConAdminKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtConAdminKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if(!Character.isDigit(c)){
            evt.consume();
        }
        if(txtConAdmin.getText().length()>=10){
            evt.consume();
        }
    }//GEN-LAST:event_txtConAdminKeyTyped

