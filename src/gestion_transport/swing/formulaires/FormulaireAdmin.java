    private void btnAjoutAdminActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjoutAdminActionPerformed
        // TODO add your handling code here:
         if (!verifierAdmin()){
            return ;
        }
        else {
            String nom = txtNomAdmin.getText();
            String contact = txtConAdmin.getText();
            String mdp = new String(txtMdpAdmin.getPassword());
            
            Admin ad = new Admin(nom, contact, mdp, this.id_coop);
            try {
                GestionAdmin ga = new GestionAdmin();
                ga.insertAdmin(ad);
                EffacerChampsAdmin();
                JOptionPane.showMessageDialog(txtNomAdmin, "Insertion Admin réussi!!. On va vous rediriger pour valider la création de votre compte Admin. Merci!!");
                new FormulaireLoginAdmin().setVisible(true);
                dispose();
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage(), "Erreur", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnAjoutAdminActionPerformed

