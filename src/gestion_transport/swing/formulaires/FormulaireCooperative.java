    private void btnAjoutCoopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjoutCoopActionPerformed
        // TODO add your handling code here:
         if (!verifierChamps()){
            return;
        }
        else {
            String nom = txtNomCoop.getText();
            String contact = txtContact.getText();
            String numporte = txtNumPorte.getText();
            String logo = txtLogo.getText();
            Cooperative cp = new Cooperative(nom, contact, logo, numporte);
            try {
                GestionCooperative gc = new GestionCooperative();
                int id_coop = gc.insertCoop(cp);
                EffacerChamps();
                JOptionPane.showMessageDialog(txtNomCoop, "Insertion Cooperative réussi!!");
                FormulaireAdmin fa = new FormulaireAdmin(id_coop);
                fa.setVisible(true);
                
                dispose();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage(), "Erreur", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnAjoutCoopActionPerformed

