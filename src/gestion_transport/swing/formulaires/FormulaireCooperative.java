    private void txtNomCoopKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNomCoopKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if(!Character.isLetterOrDigit(c) && c != ' '){
            evt.consume();
        }
    }//GEN-LAST:event_txtNomCoopKeyTyped

    private void txtNumPorteKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNumPorteKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if(!Character.isDigit(c)){
            evt.consume();
        }
    }//GEN-LAST:event_txtNumPorteKeyTyped

    private void txtContactKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtContactKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if(!Character.isDigit(c)){
            evt.consume();
        }
        if(txtContact.getText().length()>=10){
            evt.consume();
        }
    }//GEN-LAST:event_txtContactKeyTyped

