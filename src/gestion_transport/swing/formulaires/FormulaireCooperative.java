    private boolean verifierChamps(){
        if(txtNomCoop.getText().trim().isEmpty() || txtContact.getText().trim().isEmpty() || txtLogo.getText().trim().isEmpty() || txtNumPorte.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Veuiller remplir tous les champs obligatoires", "Champs manquants", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        if(!txtContact.getText().trim().matches("^(034|032|033|038|037)\\d{7}$")){
            JOptionPane.showMessageDialog(this, "Le contact doit comporter 10 chiffres et commencer par 034, 032, 033, 038 ou 037", "Numéro Invalide", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        return true;
    }
    
    public void EffacerChamps(){
        txtNomCoop.setText("");
        txtContact.setText("");
        txtNumPorte.setText("");
        txtLogo.setText("");
    }
    
